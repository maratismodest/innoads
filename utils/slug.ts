export default function slug(word: string) {
  let answer = '';

  for (const letter of word) {
    answer += rules.hasOwnProperty(letter) ? rules[letter] : letter;
  }

  //exclude letters digits - and empty
  return answer
    .toLowerCase()
    .replace(/[^\w ]+/g, '')
    .replace(/ +/g, '-');
}
